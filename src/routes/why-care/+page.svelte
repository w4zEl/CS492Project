	<h1 class="my-[0.67em] text-2xl font-bold leading-tight">Why Care?</h1>
	<p class="my-[1em] text-base font-normal leading-relaxed">
		At first glance, it might seem harmless—after all, you may feel your data is safe amongst the data of everyone else. But even anonymized or aggregated data can be misused, putting your privacy, security, and personal finances at risk.
	</p>

	<h2 class="my-[0.67em] text-xl font-semibold leading-tight">Identity Theft from Data Breaches</h2>
	<p class="my-[1em] text-base font-normal leading-relaxed">
		Massive leaks like the Equifax breach exposed sensitive information of over 143 million people, leading to long-term consequences like fraud and stolen identities.
	</p>

	<h2 class="my-[0.67em] text-xl font-semibold leading-tight">Government Surveillance</h2>
	<p class="my-[1em] text-base font-normal leading-relaxed">
		Collected data can be accessed or requested by governments. In some cases, it’s even sold to foreign entities.
	</p>

	<h2 class="my-[0.67em] text-xl font-semibold leading-tight">Price Discrimination</h2>
	<p class="my-[1em] text-base font-normal leading-relaxed">
		Companies can analyze your behavior to determine how much you're willing to pay—then charge you more than others for the same product or service.
	</p>
